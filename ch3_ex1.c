#include <stdio.h>

int main() {
    // variable declarations
    int i, j; // declares two integer variables: i and j
    float x, y; // declares two floating-point variables: x and y
    
    // variable assignments
    i = 10; // assigns the value 10 to i
    j = 20; // assigns the value 20 to j
    x = 43.2892f; // assigns the value 43.2892 to x
    y = 5527.0f; // assigns the value 5527.0 to y
    
    // prints the variable values
    printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y);

    return 0; // indicates a successful program execution by returning 0
}
